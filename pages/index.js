export async function getStaticProps() {
  const result = await Stack.getEntryWithRef('home', ['banner', 'sections'], 'en-us');
  const navitems = await Stack.getSpecificEntryKeyValue(
    "navigation_menu",
    "menu_location",
    "header",
    "en-us"
  );
  const featuredProps = await fetch(`${process.env.NEXT_PUBLIC_APP_BASE_URL}/api/property/featured-properties`);
  const properties = await featuredProps.json();
  console.log("ðŸš€ ~ file: index.js:33 ~ getStaticProps ~ properties", properties)
  
  return {
    props: {
      data: result[0],
      navigation: navitems[0],
      featuredProps : properties.data
    }
  }
}
